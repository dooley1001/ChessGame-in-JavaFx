		public ArrayList<Point> availableMoves(Tile tiles[][], Point from){
			 ArrayList<Point> availableMoves = new ArrayList<Point>();
	
				loop :for(int i=0; i<=Moves.size()-7; i+=7){
	
			 	innerloop :for(int k=0; k<=7; k++){
			 		if(k+i>=Moves.size())
			 		 break loop;
			 		 Point move = Moves.get(k+i);
			 		
			 		 if(from.y+move.y > 7 || from.y+move.y < 0 ||	from.x+move.x > 7 || from.x+move.x < 0)
			 				continue;
			 		 if(tiles[from.y+move.y][from.x+move.x].piece == null){
			 			availableMoves.add(move);
			 		}
			 		 else if(tiles[from.y+move.y][from.x+move.x].piece.color == color){
			 			break innerloop;
			 		}
			 		 else if(tiles[from.y+move.y][from.x+move.x].piece.color != color){
			 			availableMoves.add(move);
			 			break innerloop;
			 		}
			 	}
			 }
			 return availableMoves;
		}
