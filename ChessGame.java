		public void mouseClicked(MouseEvent e){ 
			
		Point currentTile = new Point(e.getX()/tileSize,e.getY()/tileSize);

			if(MouseClick.x==currentTile.x && MouseClick.y== currentTile.y){
				MouseClick.setLocation(-1,-1);
				repaint();
				return;
			}
			else if(MouseClick.x==-1 && MouseClick.y==-1){
				Tile check = tiles[currentTile.y][currentTile.x];
				if(check.piece.color == lastMoved)
    				return;
				MouseClick.setLocation (currentTile.x,currentTile.y);
				repaint();
			}
			else{
				
				System.out.println("If possible perform a move");
				System.out.println("Move "+MouseClick.x+" "+MouseClick.y+" to "+currentTile.x+" "+currentTile.y);
				
				Tile ActiveTile = tiles[MouseClick.y][MouseClick.x];
				if(ActiveTile.piece==null)
					return;
				if(ActiveTile.piece.color == lastMoved)
    			return;
				
				for(Point move : ActiveTile.piece.availableMoves(tiles, MouseClick)){
					if(move.x+MouseClick.x==currentTile.x && move.y+MouseClick.y==currentTile.y){
						
						//checking to see if any pieces can be taken from the board when clicked 
						Tile swap_temp = tiles[currentTile.y][currentTile.x];;
						swap_temp.piece = null;
						tiles[currentTile.y][currentTile.x] = tiles[MouseClick.y][MouseClick.x];
						tiles[MouseClick.y][MouseClick.x] = swap_temp;
						Tile moved_piece = tiles[currentTile.y][currentTile.x];
						
						if(moved_piece.piece.symbol()=="\u265F" && currentTile.y==7 && moved_piece.piece.color == Color.WHITE){
							moved_piece.piece.Moves=queenRef.Moves;
							 moved_piece.piece.symbol=queenRef.symbol();
						
						}
						else if(moved_piece.piece.symbol()=="\u265F" && currentTile.y==0 && moved_piece.piece.color == Color.BLACK){
							moved_piece.piece.Moves=queenRef.Moves;
							 moved_piece.piece.symbol=queenRef.symbol();
						}
						else if(moved_piece.piece.symbol()=="\u265F"){
							moved_piece.piece.updatePiece(0);
						}
						lastMoved = moved_piece.piece.color;
				     	MouseOver.x = -1;
						MouseOver.y = -1;
						MouseClick.x = -1;
						MouseClick.y = -1;
						MouseClickHover.x = -1;
						MouseClickHover.y = -1;
						
					repaint();
					}
				}
			}
		}
