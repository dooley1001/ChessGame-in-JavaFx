    		Tile currentTile = tiles[(int)currentTilePoint.getY()][(int)currentTilePoint.getX()];
    		if(currentTile.piece==null)
    			return;
    		if(currentTile.piece.color == lastMoved)
    			return;


    		Color green = new Color(0, 255,0, 128 );
